    /**
     * 显示资源列表
     *
     * @return \think\Response
     */
    public function index()
    {
//        try {
        $model = model('Goodsmodel');

        $data = $this->request->get();

        if (isset($data['page']) && !empty($data['page'])) {
            $page = $data['page'];
        } else {
            $page = 1;
        }
        if (isset($data['limit']) && !empty($data['limit'])) {
            $limit = $data['limit'];
        } else {
            $limit = 3;
        }

        $sarr = [];

        if (!empty($data['gname'])) {
            $sarr['gname'] = ['like', '%' . $data['gname'] . '%'];
        }
        if (!empty($data['gename'])) {
            $sarr['gename'] = ['like', '%' . $data['gename'] . '%'];
        }
        if (!empty($data['lowprice']) && !empty($data['highprice']) && $data['lowprice'] < $data['highprice']) {
            $sarr['price'] = [
                    ['>',$data['lowprice']],
                    ['<',$data['highprice']]
            ];
        }


        $goods = $model->querys($sarr,$page, $limit);

//        echo $model->getLastSql();
        $total = $goods->total();
        $data = $goods->items();

        if ($total) {
            return json([
                'code' => config('code.success'),
                'msg' => '商品获取成功',
                'data' => $data,
                'total' => $total
            ]);
        } else {
            return json([
                'code' => config('code.success'),
                'msg' => '暂无商品',
                'data' => []
            ]);
        }
//        }catch (Exception $exception){
//            return json([
//                'code' => config('code.fail'),
//                'msg' => '商品获取失败'
//            ]);
//        }
    }

